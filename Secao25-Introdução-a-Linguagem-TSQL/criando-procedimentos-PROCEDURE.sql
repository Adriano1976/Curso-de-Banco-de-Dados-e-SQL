CREATE PROCEDURE NUMERO @NUM1 INT = 2000
AS  
BEGIN
	IF @NUM1 = 2000
		PRINT 'O VALOR É VERDADEIRO'
	ELSE  
		PRINT 'O VALOR É FALSO'
END  
GO  



CREATE PROCEDURE CONDICIONAL @CONTADOR INT
AS  
BEGIN
	SELECT 
	CASE 
		WHEN FABRICANTE = 'FIAT' THEN 'FAIXA 1'
		WHEN FABRICANTE = 'CHEVROLET' THEN 'FAIXA 2'
		ELSE 'OUTRAS FAIXAS'
	END AS 'INFORMACOES', 
	* 
	FROM CARROS		
END  
GO  


